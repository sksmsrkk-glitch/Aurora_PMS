/** Generates deterministic repository metrics; never hand-edit its output. */
import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { actionRegistry } from "../app/api/pms/action-registry.ts";

const root = process.cwd();
const read = (file) => readFile(path.join(root, file), "utf8");
const migrationNames = (await readdir(path.join(root, "supabase", "migrations")))
  .filter((name) => /^\d+_.+\.sql$/u.test(name))
  .sort();
const migrationSql = (await Promise.all(migrationNames.map((name) => read(`supabase/migrations/${name}`)))).join("\n");

function uniqueMatches(source, expression) {
  return new Set(Array.from(source.matchAll(expression), (match) => match[1].toLowerCase()));
}

const tables = uniqueMatches(migrationSql, /CREATE\s+TABLE\s+(?:IF\s+NOT\s+EXISTS\s+)?(?:public\.)?["']?([a-z_][a-z0-9_]*)["']?/giu);
const triggers = uniqueMatches(migrationSql, /CREATE\s+TRIGGER\s+["']?([a-z_][a-z0-9_]*)["']?/giu);
const foreignKeys = uniqueMatches(migrationSql, /CONSTRAINT\s+["']?([a-z_][a-z0-9_]*)["']?\s+FOREIGN\s+KEY/giu);
const rlsTables = uniqueMatches(migrationSql, /ALTER\s+TABLE\s+(?:public\.)?["']?([a-z_][a-z0-9_]*)["']?\s+ENABLE\s+ROW\s+LEVEL\s+SECURITY/giu);
const tenantMigration = await read("supabase/migrations/202607170010_tenant_context_rls.sql");
const tenantArray = tenantMigration.match(/tables\s+text\[\]\s*:=\s*ARRAY\[(.*?)\];/su)?.[1] || "";
for (const match of tenantArray.matchAll(/'([a-z_][a-z0-9_]*)'/gu)) rlsTables.add(match[1]);

const testFiles = (await readdir(path.join(root, "tests"))).filter((name) => /\.test\.mjs$|\.integration\.mjs$/u.test(name));
let unitTests = 0;
let integrationTests = 0;
for (const name of testFiles) {
  const count = (await read(`tests/${name}`)).match(/^\s*test\s*\(/gmu)?.length || 0;
  if (name.includes(".integration.")) integrationTests += count;
  else unitTests += count;
}

const cssFiles = ["app/globals.css", ...(await readdir(path.join(root, "app", "styles"))).filter((name) => name.endsWith(".css")).map((name) => `app/styles/${name}`)];
// Git may materialize CRLF on Windows and LF in CI. Count canonical UTF-8
// source bytes so the generated contract is identical on every platform.
const canonicalBytes = async (file) => Buffer.byteLength((await read(file)).replace(/\r\n?/gu, "\n"), "utf8");
const pmsCssBytes = (await Promise.all(cssFiles.map(canonicalBytes))).reduce((sum, size) => sum + size, 0);
const publicCssBytes = await canonicalBytes("app/hotel/hotel.css");
const documentFiles = (await readdir(path.join(root, "docs"))).filter((name) => name.endsWith(".md"));
const format = new Intl.NumberFormat("en-US").format;
const latestMigration = migrationNames.at(-1)?.replace(/\.sql$/u, "") || "none";

const output = `<!-- Generated by npm run docs:metrics. Do not edit manually. -->
# Aurora PMS 자동 생성 프로젝트 지표

| 지표 | 현재 값 | 계산 원본 |
| --- | ---: | --- |
| Migration | ${format(migrationNames.length)} | \`supabase/migrations/*.sql\` |
| 런타임 스키마 계약 | \`${latestMigration}\` | 정렬상 마지막 migration |
| Public table 선언 | ${format(tables.size)} | 고유 \`CREATE TABLE\` |
| Application trigger 선언 | ${format(triggers.size)} | 고유 \`CREATE TRIGGER\` |
| Foreign key 선언 | ${format(foreignKeys.size)} | 고유 named FK constraint |
| Tenant RLS 대상 | ${format(rlsTables.size)} | RLS enable 선언과 tenant table catalog |
| 등록 PMS action | ${format(actionRegistry.size)} | action registry key |
| Unit/behavior test | ${format(unitTests)} | \`tests/*.test.mjs\`의 test case |
| PostgreSQL integration test | ${format(integrationTests)} | \`tests/*.integration.mjs\`의 test case |
| PMS CSS source | ${format(pmsCssBytes)} bytes / ${cssFiles.length} files | root PMS layout imports |
| Public hotel CSS source | ${format(publicCssBytes)} bytes / 1 file | isolated \`app/hotel/hotel.css\` |
| 상세 문서 | ${format(documentFiles.length)} files | \`docs/*.md\` (generated 제외) |

이 표는 소스 선언 수를 보여 줍니다. 배포 대상의 실제 catalog는 \`npm run db:contract:verify\`와 \`npm run db:supabase:smoke\`가 별도로 검증합니다.
`;

await mkdir(path.join(root, "docs", "generated"), { recursive: true });
await writeFile(path.join(root, "docs", "generated", "project-metrics.md"), output, "utf8");
