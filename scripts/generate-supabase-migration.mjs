/** Converts D1-compatible Drizzle output into the baseline Supabase DDL. */
import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const drizzleDirectory = path.join(root, "drizzle");
const outputDirectory = path.join(root, "supabase", "migrations");
const outputPath = path.join(outputDirectory, "202607160001_aurora_pms.sql");
const migrationId = "202607160001_aurora_pms";

const files = (await readdir(drizzleDirectory))
  .filter((name) => /^\d{4}_.+\.sql$/u.test(name) && name !== "0003_financial_integrity.sql")
  .sort();

function sqliteToPostgres(sql) {
  return sql
    .replaceAll("--> statement-breakpoint", "")
    .replaceAll("`", '"')
    .replace(/\binteger\s+PRIMARY KEY\s+AUTOINCREMENT\s+NOT NULL\b/giu, "bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL")
    .replace(/\breal\b/giu, "numeric(14,2)")
    .replace(/\bDEFAULT\s+true\b/giu, "DEFAULT 1")
    .replace(/\bDEFAULT\s+false\b/giu, "DEFAULT 0")
    .replace(/"cashier_sessions"\./gu, "")
    .trim();
}

const baseSchema = [];
for (const file of files) {
  const contents = await readFile(path.join(drizzleDirectory, file), "utf8");
  baseSchema.push(`-- Source: drizzle/${file}\n${sqliteToPostgres(contents)}`);
}

const tables = [...baseSchema.join("\n").matchAll(/CREATE TABLE\s+"([^"]+)"/giu)].map((match) => match[1]);

const postgresControls = String.raw`
-- Reporting and operational access paths.
CREATE INDEX report_reservation_filter_idx ON reservations(property_id,status,source,room_type_id,arrival_date,departure_date);
CREATE INDEX report_folio_business_idx ON folio_entries(property_id,business_date,kind,payment_method);
CREATE INDEX report_audit_created_idx ON audit_logs(property_id,created_at,action,actor);

-- Serialize inventory decisions per property/type/date to prevent concurrent oversell.
CREATE OR REPLACE FUNCTION pms_lock_inventory(p_property text, p_room_type text, p_stay_date text)
RETURNS void LANGUAGE plpgsql AS $$
BEGIN
  PERFORM pg_advisory_xact_lock(hashtextextended(p_property || ':' || p_room_type || ':' || p_stay_date, 0));
END;
$$;

CREATE OR REPLACE FUNCTION pms_reservation_capacity_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
DECLARE
  capacity_limit integer;
  sold integer;
  held integer;
  is_closed integer;
BEGIN
  PERFORM pms_lock_inventory(NEW.property_id, NEW.room_type_id, NEW.stay_date);
  SELECT COALESCE(ic.closed,0), COALESCE(ic.sell_limit,
    (SELECT COUNT(*) FROM rooms r WHERE r.property_id=NEW.property_id AND r.room_type_id=NEW.room_type_id AND r.active=1 AND r.housekeeping_status<>'OUT_OF_SERVICE'))
  INTO is_closed, capacity_limit
  FROM (SELECT 1) seed LEFT JOIN inventory_controls ic
    ON ic.property_id=NEW.property_id AND ic.room_type_id=NEW.room_type_id AND ic.stay_date=NEW.stay_date;
  IF is_closed=1 THEN RAISE EXCEPTION 'room type closed'; END IF;
  SELECT COUNT(*) INTO sold FROM reservation_type_nights
    WHERE property_id=NEW.property_id AND room_type_id=NEW.room_type_id AND stay_date=NEW.stay_date;
  SELECT COALESCE(SUM(bi.current_rooms-bi.picked_up),0) INTO held
    FROM block_inventory bi JOIN business_blocks bb ON bb.id=bi.block_id
    WHERE bi.property_id=NEW.property_id AND bi.room_type_id=NEW.room_type_id AND bi.stay_date=NEW.stay_date
      AND bb.deduct_inventory=1 AND bb.status IN ('TENTATIVE','DEFINITE');
  IF sold+held>=capacity_limit THEN RAISE EXCEPTION 'room type sold out'; END IF;
  RETURN NEW;
END;
$$;

CREATE TRIGGER reservation_type_nights_capacity BEFORE INSERT ON reservation_type_nights
FOR EACH ROW EXECUTE FUNCTION pms_reservation_capacity_guard();

CREATE OR REPLACE FUNCTION pms_block_inventory_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
DECLARE
  capacity_limit integer;
  sold integer;
  held integer;
  deducts integer;
BEGIN
  IF NEW.original_rooms<0 OR NEW.current_rooms<0 OR NEW.picked_up<0 OR NEW.current_rooms<NEW.picked_up OR NEW.rate<0 THEN
    RAISE EXCEPTION 'invalid block inventory';
  END IF;
  SELECT deduct_inventory INTO deducts FROM business_blocks WHERE id=NEW.block_id;
  IF COALESCE(deducts,0)=1 THEN
    PERFORM pms_lock_inventory(NEW.property_id, NEW.room_type_id, NEW.stay_date);
    SELECT COALESCE(ic.sell_limit,
      (SELECT COUNT(*) FROM rooms r WHERE r.property_id=NEW.property_id AND r.room_type_id=NEW.room_type_id AND r.active=1 AND r.housekeeping_status<>'OUT_OF_SERVICE'))
    INTO capacity_limit
    FROM (SELECT 1) seed LEFT JOIN inventory_controls ic
      ON ic.property_id=NEW.property_id AND ic.room_type_id=NEW.room_type_id AND ic.stay_date=NEW.stay_date;
    SELECT COUNT(*) INTO sold FROM reservation_type_nights
      WHERE property_id=NEW.property_id AND room_type_id=NEW.room_type_id AND stay_date=NEW.stay_date;
    SELECT COALESCE(SUM(bi.current_rooms-bi.picked_up),0) INTO held
      FROM block_inventory bi JOIN business_blocks bb ON bb.id=bi.block_id
      WHERE bi.property_id=NEW.property_id AND bi.room_type_id=NEW.room_type_id AND bi.stay_date=NEW.stay_date
        AND (TG_OP='INSERT' OR bi.id<>OLD.id) AND bb.deduct_inventory=1 AND bb.status IN ('TENTATIVE','DEFINITE');
    IF sold+held+(NEW.current_rooms-NEW.picked_up)>capacity_limit THEN RAISE EXCEPTION 'block inventory sold out'; END IF;
  END IF;
  RETURN NEW;
END;
$$;

CREATE TRIGGER block_inventory_capacity_insert BEFORE INSERT ON block_inventory
FOR EACH ROW EXECUTE FUNCTION pms_block_inventory_guard();
CREATE TRIGGER block_inventory_capacity_update BEFORE UPDATE ON block_inventory
FOR EACH ROW EXECUTE FUNCTION pms_block_inventory_guard();

CREATE OR REPLACE FUNCTION pms_block_pickup_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  PERFORM pms_lock_inventory(NEW.property_id, NEW.room_type_id, NEW.stay_date);
  IF NOT EXISTS (SELECT 1 FROM block_inventory WHERE block_id=NEW.block_id AND room_type_id=NEW.room_type_id AND stay_date=NEW.stay_date AND picked_up<current_rooms) THEN
    RAISE EXCEPTION 'block allocation exhausted';
  END IF;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION pms_block_pickup_apply()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  IF TG_OP='INSERT' THEN
    UPDATE block_inventory SET picked_up=picked_up+1,version=version+1,updated_at=NEW.created_at
      WHERE block_id=NEW.block_id AND room_type_id=NEW.room_type_id AND stay_date=NEW.stay_date;
    RETURN NEW;
  END IF;
  UPDATE block_inventory SET picked_up=GREATEST(0,picked_up-1),version=version+1,updated_at=clock_timestamp()::text
    WHERE block_id=OLD.block_id AND room_type_id=OLD.room_type_id AND stay_date=OLD.stay_date;
  RETURN OLD;
END;
$$;

CREATE TRIGGER block_pickup_validate BEFORE INSERT ON block_pickup_nights FOR EACH ROW EXECUTE FUNCTION pms_block_pickup_guard();
CREATE TRIGGER block_pickup_increment AFTER INSERT ON block_pickup_nights FOR EACH ROW EXECUTE FUNCTION pms_block_pickup_apply();
CREATE TRIGGER block_pickup_decrement AFTER DELETE ON block_pickup_nights FOR EACH ROW EXECUTE FUNCTION pms_block_pickup_apply();

CREATE OR REPLACE FUNCTION pms_inventory_control_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.sell_limit<0 OR NEW.min_stay<1 OR NEW.price_override<0 THEN RAISE EXCEPTION 'invalid inventory control'; END IF;
  RETURN NEW;
END;
$$;
CREATE TRIGGER inventory_controls_validate_insert BEFORE INSERT ON inventory_controls FOR EACH ROW EXECUTE FUNCTION pms_inventory_control_guard();
CREATE TRIGGER inventory_controls_validate_update BEFORE UPDATE ON inventory_controls FOR EACH ROW EXECUTE FUNCTION pms_inventory_control_guard();

CREATE OR REPLACE FUNCTION pms_folio_entry_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.amount<=0 OR NEW.kind NOT IN ('CHARGE','PAYMENT','CHARGE_REVERSAL','PAYMENT_REVERSAL','REFUND') THEN RAISE EXCEPTION 'invalid folio entry'; END IF;
  RETURN NEW;
END;
$$;
CREATE TRIGGER folio_entries_validate_insert BEFORE INSERT ON folio_entries FOR EACH ROW EXECUTE FUNCTION pms_folio_entry_guard();

CREATE OR REPLACE FUNCTION pms_folio_detail_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
DECLARE entry_amount numeric(14,2);
BEGIN
  SELECT amount INTO entry_amount FROM folio_entries WHERE id=NEW.entry_id;
  IF NEW.net_amount<0 OR NEW.tax_amount<0 OR NEW.service_amount<0 OR ABS((NEW.net_amount+NEW.tax_amount+NEW.service_amount)-entry_amount)>0.011
    OR NOT EXISTS (SELECT 1 FROM folio_windows WHERE id=NEW.folio_window_id AND reservation_id=NEW.reservation_id AND status='OPEN')
  THEN RAISE EXCEPTION 'invalid folio detail'; END IF;
  RETURN NEW;
END;
$$;
CREATE TRIGGER folio_details_validate_insert BEFORE INSERT ON folio_entry_details FOR EACH ROW EXECUTE FUNCTION pms_folio_detail_guard();

CREATE OR REPLACE FUNCTION pms_ar_ledger_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.debit<0 OR NEW.credit<0 OR (NEW.debit=0 AND NEW.credit=0) OR (NEW.debit>0 AND NEW.credit>0)
    OR NEW.kind NOT IN ('INVOICE','PAYMENT','CREDIT','ADJUSTMENT','REVERSAL') THEN RAISE EXCEPTION 'invalid ar ledger entry'; END IF;
  RETURN NEW;
END;
$$;
CREATE TRIGGER ar_ledger_validate_insert BEFORE INSERT ON ar_ledger_entries FOR EACH ROW EXECUTE FUNCTION pms_ar_ledger_guard();

CREATE OR REPLACE FUNCTION pms_integration_attempt_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.attempt_no<1 OR NEW.direction NOT IN ('INBOUND','OUTBOUND') OR NEW.status NOT IN ('ACKED','FAILED') THEN RAISE EXCEPTION 'invalid integration attempt'; END IF;
  RETURN NEW;
END;
$$;
CREATE TRIGGER integration_attempts_validate_insert BEFORE INSERT ON integration_delivery_attempts FOR EACH ROW EXECUTE FUNCTION pms_integration_attempt_guard();

CREATE OR REPLACE FUNCTION pms_immutable_guard()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  RAISE EXCEPTION '%', TG_ARGV[0];
END;
$$;
CREATE TRIGGER folio_entries_no_update BEFORE UPDATE ON folio_entries FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('folio entries are immutable');
CREATE TRIGGER folio_entries_no_delete BEFORE DELETE ON folio_entries FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('folio entries are immutable');
CREATE TRIGGER folio_details_no_update BEFORE UPDATE ON folio_entry_details FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('folio details are immutable');
CREATE TRIGGER folio_details_no_delete BEFORE DELETE ON folio_entry_details FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('folio details are immutable');
CREATE TRIGGER ar_ledger_no_update BEFORE UPDATE ON ar_ledger_entries FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('ar ledger entries are immutable');
CREATE TRIGGER ar_ledger_no_delete BEFORE DELETE ON ar_ledger_entries FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('ar ledger entries are immutable');
CREATE TRIGGER integration_attempts_no_update BEFORE UPDATE ON integration_delivery_attempts FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('integration attempts are immutable');
CREATE TRIGGER integration_attempts_no_delete BEFORE DELETE ON integration_delivery_attempts FOR EACH ROW EXECUTE FUNCTION pms_immutable_guard('integration attempts are immutable');
`;

const securitySql = tables.map((table) => `ALTER TABLE public."${table}" ENABLE ROW LEVEL SECURITY;\nREVOKE ALL ON TABLE public."${table}" FROM anon, authenticated;`).join("\n");

const migration = `-- Generated by scripts/generate-supabase-migration.mjs. Do not edit by hand.\nBEGIN;\nSET LOCAL lock_timeout = '10s';\nSET LOCAL statement_timeout = '120s';\nCREATE TABLE IF NOT EXISTS pms_schema_migrations (id text PRIMARY KEY, applied_at timestamptz NOT NULL DEFAULT now());\n\n${baseSchema.join("\n\n")}\n\n${postgresControls}\n\n-- The browser-facing Supabase roles have no direct PMS table access; all access is server mediated.\n${securitySql}\nREVOKE ALL ON ALL SEQUENCES IN SCHEMA public FROM anon, authenticated;\nINSERT INTO pms_schema_migrations(id) VALUES ('${migrationId}') ON CONFLICT (id) DO NOTHING;\nCOMMIT;\n`;

await mkdir(outputDirectory, { recursive: true });
await writeFile(outputPath, migration, "utf8");
console.log(`Generated ${path.relative(root, outputPath)} (${tables.length} tables, ${files.length} source migrations).`);
